def move(n, x, y):
    if n == 1:
        print('Переложить диск 1, со стержня номер {}, на стержень номер {}'.format(x, y))
    else:
        move(n - 1, x, 6 - x - y)
        print('Переложить диск {}, со стержня номер {}, на стержень номер {}'.format(n, x, y))
        move(n - 1, 6 - x - y, y)


num = int(input('Введите количество дисков: '))
move(num, 1, 3)
